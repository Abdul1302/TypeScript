//var:
// var variables can be updated and re-declared
// within its scope

var Abd='Abdul Rehman'
console.log(Abd)

//let:
//let variables can be updated but not 
//re-declared

let number=34
console.log(number)

//const:
//const variables can neither be updated 
//nor re-declared.

const pi=3.14
console.log(pi)